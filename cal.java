    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String bqt1=Integer.toBinaryString(Integer.parseInt(qt1.getText()));
        String bqt2=Integer.toBinaryString(Integer.parseInt(qt2.getText()));
        String bqt3=Integer.toBinaryString(Integer.parseInt(qt3.getText()));
        String bqt4=Integer.toBinaryString(Integer.parseInt(qt4.getText()));

        String ipByte1=String.format("%08d", Integer.parseInt(bqt1));
        String ipByte2=String.format("%08d", Integer.parseInt(bqt2));
        String ipByte3=String.format("%08d", Integer.parseInt(bqt3));
        String ipByte4=String.format("%08d", Integer.parseInt(bqt4));

        int bitm=Integer.valueOf(cidr.getText());

        int ipBin=Integer.parseInt(ipByte1+ipByte2+ipByte3,2);

        ipBin=(ipBin << 8)+Integer.parseInt(ipByte4,2);

        int maskBin= ~0 << (32-bitm);

        int netBin=ipBin & maskBin;

        int bcBin=ipBin | ~maskBin;

        String strIpBin =Integer.toBinaryString(ipBin);
        strIpBin=String.format("%32s", strIpBin).replace(" ", "0");
        String[] printIpBin=strIpBin.split("(?<=\\G........)");

        String strMaskBin =Integer.toBinaryString(maskBin);
        strMaskBin=String.format("%32s", strMaskBin).replace(" ", "0");
        String[] printMaskBin=strMaskBin.split("(?<=\\G........)");

        String strNetBin =Integer.toBinaryString(netBin);
        strNetBin=String.format("%32s", strNetBin).replace(" ", "0");
        String[] printNetBin=strNetBin.split("(?<=\\G........)");

        String strBroBin =Integer.toBinaryString(bcBin);
        strBroBin=String.format("%32s", strBroBin).replace(" ", "0");
        String[] printBroBin=strBroBin.split("(?<=\\G........)");

        binIP.setText(printIpBin[0]+"."+printIpBin[1]+"."+printIpBin[2]+"."+printIpBin[3]);
        binSub.setText(printMaskBin[0]+"."+printMaskBin[1]+"."+printMaskBin[2]+"."+printMaskBin[3]);
        binNet.setText(printNetBin[0]+"."+printNetBin[1]+"."+printNetBin[2]+"."+printNetBin[3]);
        binBroad.setText(printBroBin[0]+"."+printBroBin[1]+"."+printBroBin[2]+"."+printBroBin[3]);

        sub.setText(
            String.valueOf(Integer.parseInt(printMaskBin[0],2))+"."+
            String.valueOf(Integer.parseInt(printMaskBin[1],2))+"."+
            String.valueOf(Integer.parseInt(printMaskBin[2],2))+"."+
            String.valueOf(Integer.parseInt(printMaskBin[3],2))

        );
        net.setText(
            String.valueOf(Integer.parseInt(printNetBin[0],2))+"."+
            String.valueOf(Integer.parseInt(printNetBin[1],2))+"."+
            String.valueOf(Integer.parseInt(printNetBin[2],2))+"."+
            String.valueOf(Integer.parseInt(printNetBin[3],2))

        );

        broad.setText(
            String.valueOf(Integer.parseInt(printBroBin[0],2))+"."+
            String.valueOf(Integer.parseInt(printBroBin[1],2))+"."+
            String.valueOf(Integer.parseInt(printBroBin[2],2))+"."+
            String.valueOf(Integer.parseInt(printBroBin[3],2))

        );

        toHo.setText(String.valueOf(~maskBin - 1));
        toNet.setText(String.valueOf(~(~0 << bitm)+1));

        int nc = Integer.parseInt(qt1.getText());

        if (nc > 0 && nc < 128){
            ncls.setText("A");
        }
        else if (nc > 127 && nc <192){
            ncls.setText("B");
        }
        else if (nc > 191 && nc <224){
            ncls.setText("C");

        }
        else ncls.setText(" ");

    }//GEN-LAST:event_jButton1MouseClicked

